#define DECAY 4
int decay = 0;
// Sample interrupt
ISR(TIMER0_COMPA_vect) {
	OCR1A = wav[p]; 
	if ( decay == 0 )
		wav[p] = (wav[p] >> 1) + (wav[last_p] >> 1);
	last_p = p++;
	if (p == wav_len) {
		p = 0;
		if (decay++ == DECAY) decay = 0;
	}
}
